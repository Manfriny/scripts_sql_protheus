--sintético
select GX3_FILIAL FILIAL,GX3_XDESCO OPERACAO,
COUNT(DISTINCT GX4_NRMOV )QTD_ENTRADA_DIA,--VEÍCULOS QUE FORAM FEITOS O ACESSO NA DATA
COUNT(CASE WHEN GX3_SIT = 6 THEN 1 END ) QTD_CONCLUIDO,
COUNT(CASE WHEN GX3_SIT = 2 THEN 1 WHEN GX3_SIT =3 THEN 1 WHEN GX3_SIT = 4 THEN 1 WHEN GX3_SIT = 1 THEN 1 END ) QTD_PROCESSANDO,
COUNT(CASE WHEN GX3_SIT = 5 THEN 1 END ) QTD_CANCELADO
FROM (
SELECT DISTINCT gx4_nrmov,gx3_filial, gx3_cdoper, gx3_seq,gx3_xdesco,GX3_DTCRIA,GX3_SIT
FROM GX3010 GX3 
INNER JOIN GX4010 GX4 ON GX4.GX4_NRMOV = GX3.GX3_NRMOV AND GX4.D_E_L_E_T_ <> '*' AND GX4.GX4_FILIAL = GX3.GX3_FILIAL 
WHERE  GX3.D_E_L_E_T_ <> '*' 
AND GX3.GX3_DTCRIA >= '20170727' AND GX3.GX3_DTCRIA <= '20170727' AND GX3.GX3_CDOPER >= '                ' AND GX3.GX3_SEQ >= '   ' 
AND GX3.GX3_CDOPER <= 'ZZZZZZZZZZZZZZZZ' AND GX3.GX3_SEQ <= 'ZZ ' AND GX3.GX3_FILIAL >= '    ' AND GX3.GX3_FILIAL <= 'ZZ  ' )XX
GROUP BY GX3_FILIAL,GX3_XDESCO;

